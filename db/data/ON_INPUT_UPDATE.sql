DROP TRIGGER IF EXISTS `ON_INPUT_UPDATE`;
CREATE DEFINER=`root`@`%` TRIGGER `ON_INPUT_UPDATE` AFTER UPDATE ON `my_ecommerce_BIT8_CARD` FOR EACH ROW 
BEGIN
 DECLARE DI_0 BOOLEAN;
 DECLARE DI_1 BOOLEAN;
 DECLARE DI_2 BOOLEAN;
 DECLARE DI_3 BOOLEAN;
 DECLARE DI_4 BOOLEAN;
 DECLARE DI_5 BOOLEAN;
 DECLARE DI_6 BOOLEAN;
 DECLARE DI_7 BOOLEAN;
 
 DECLARE DO_0 BOOLEAN;
 DECLARE DO_1 BOOLEAN;
 DECLARE DO_2 BOOLEAN;
 DECLARE DO_3 BOOLEAN;
 DECLARE DO_4 BOOLEAN;
 DECLARE DO_5 BOOLEAN;
 DECLARE DO_6 BOOLEAN;
 DECLARE DO_7 BOOLEAN;
 
 SELECT A.DIO_0, A.DIO_1, A.DIO_2, A.DIO_3, A.DIO_4, A.DIO_5, A.DIO_6, A.DIO_7 
 INTO DI_0, DI_1, DI_2, DI_3, DI_4, DI_5, DI_6, DI_7
 FROM my_ecommerce_BIT8_CARD_INPUT AS A
 WHERE BIT8_CARD_ID = 1; 
 
 CALL BCD7SEG(DI_0, DI_1, DI_2, DI_3, DI_4, DI_5, DI_6, DI_7, @DO_0, @DO_1, @DO_2, @DO_3, @DO_4, @DO_5, @DO_6, @DO_7);
 
 UPDATE my_ecommerce_BIT8_CARD_OUTPUT
 SET DIO_0 = @DO_0,
 	 DIO_1 = @DO_1,
     DIO_2 = @DO_2,
     DIO_3 = @DO_3,
     DIO_4 = @DO_4,
     DIO_5 = @DO_5,
     DIO_6 = @DO_6,
     DIO_7 = @DO_7
 WHERE BIT8_CARD_ID = 1;
END